    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $inputs = $request->all();

        if (!$inputs) {
            $request->merge([
                'is_open' => 1,
                'is_closed' => 1,
            ]);
        }

        $posts = Post::search($inputs)
                    ->paginate(10);

        return view('posts.index')
                    ->with(compact(
                        'posts',
                        'request'
                    ));
    }

